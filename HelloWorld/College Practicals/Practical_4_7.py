# Read the list of numbers from the user
lst = list(map(int, input("Enter a list of numbers: ").split()))
# Initialize a variable to store the sum
total = 0
# Use a for loop to iterate over the elements of the list and calculate the sum
for num in lst:
  total += num
# Print the sum
print(f"Sum of the numbers: {total}")
