even_numbers = [x for x in range(1, 51) if x % 2 == 0]
odd_numbers = [x for x in range(1, 51) if x % 2 != 0]
print("list of even numbers upto 50\n")
print(even_numbers)
print("\nlist of odd numbers upto 50\n")
print(odd_numbers)
divided_by_5 = [x for x in range(1, 101) if x % 5 == 0]
print("list of numbers that are divided by 5 upto 100")
print(divided_by_5)

